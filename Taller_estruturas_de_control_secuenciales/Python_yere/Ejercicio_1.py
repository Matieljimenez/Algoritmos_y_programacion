"""
Entradas
Edad_uno-->int-->Edad_uno
Edad_dos-->int-->Edad_dos
Edad_tres-->int-->Edad_tres
Salidas
Edad_promedio-->float-->Edad_promedio
"""
Edad_uno=int(input("Ingrese la primerar edad a promediar "))
Edad_dos=int(input("Ingrese la segunda edad a promediar "))
Edad_tres=int(input("Ingrese la tercera edad a promediar "))
Edad_promedio=(Edad_uno+Edad_dos+Edad_tres)/3
print("La edad promedio es: "+str(Edad_promedio))